package assignment_javaprograms;

public class Static_15 {

	
		static void methodOne(int a) {
	        System.out.println("Static Method Int: " + a);
	    }
	    static void methodTwo(String b) {
	        System.out.println("Static Method Two String: " + b);
	    }
	    static void methodThree(double c) {
	        System.out.println("Static Method Three number: " + c);
	    }
	    public static void main(String[] args) {
	        methodOne(10);
	        methodTwo("Hello World");
	        methodThree(3.14);
	    }
	

	}


