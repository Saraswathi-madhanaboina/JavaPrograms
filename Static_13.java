package assignment_javaprograms;

public class Static_13 {

		    void methodOne() {
		        System.out.println("Grow the plants");
		    }
		    void methodTwo() {
		        System.out.println("Water the plants");
		    }
		    static void staticMethod() {
		        System.out.println("Static Method is called");
		    }
		    public static void main(String[] args) {
		        Static_13 S1 = new Static_13();
		        S1.methodOne();
		        S1.methodTwo();
		        staticMethod(); 
		    }
		


	}


